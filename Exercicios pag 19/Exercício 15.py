#Tentativa de resolução utilizando o método de semelhança de triângulos
#Como há uma trena, é medido somente o que está ao alcance da pessoa
#Ou seja, sua altura, comprimento da sua sombra e da caixa d'água
#A incidência do sol forma o mesmo ângulo com a sua altura e a da caixa d'água
#Portanto, podemos seguir o critério de semelhança ângulo-ângulo
your_height=float(input("Meça sua altura com a trena e insira o valor ")) 
your_shadow=float(input("Insira o comprimento da sombra "))
object_shadow=float(input("Meça o comprimento da sombra da caixa d'agua "))
object_height=your_height*object_shadow/your_shadow
print("A altura da caixa d'água é", object_height, "metros")

                  
                  
                  
